/**
 * @brief  ETH MSP Init: configure RMII GPIO pins and ETH clocks.
 *
 * RMII pin mapping (STM32H723, NUCLEO-H723ZG style):
 *   PA1  = ETH_REF_CLK  (50 MHz from MCO2 loopback)
 *   PA7  = ETH_CRS_DV
 *   PB11 = ETH_TX_EN
 *   PB12 = ETH_TXD0
 *   PB13 = ETH_TXD1
 *   PC4  = ETH_RXD0
 *   PC5  = ETH_RXD1
 *
 * MDC/MDIO (PA2/PC1) are NOT connected in this design (LAN9370 uses SPI).
 */
void HAL_ETH_MspInit(ETH_HandleTypeDef *heth)
{
    GPIO_InitTypeDef GPIO_InitStruct = {0};

    if (heth->Instance == ETH)
    {
        __HAL_RCC_ETH1MAC_CLK_ENABLE();
        __HAL_RCC_ETH1TX_CLK_ENABLE();
        __HAL_RCC_ETH1RX_CLK_ENABLE();

        __HAL_RCC_GPIOA_CLK_ENABLE();
        __HAL_RCC_GPIOB_CLK_ENABLE();
        __HAL_RCC_GPIOC_CLK_ENABLE();

        GPIO_InitStruct.Mode      = GPIO_MODE_AF_PP;
        GPIO_InitStruct.Pull      = GPIO_NOPULL;
        GPIO_InitStruct.Speed     = GPIO_SPEED_FREQ_VERY_HIGH;
        GPIO_InitStruct.Alternate = GPIO_AF11_ETH;

        /* PA1=REF_CLK, PA2=MDIO, PA7=CRS_DV */
        GPIO_InitStruct.Pin = GPIO_PIN_1 | GPIO_PIN_2 | GPIO_PIN_7;
        HAL_GPIO_Init(GPIOA, &GPIO_InitStruct);

        /* PB11=TX_EN, PB12=TXD0, PB13=TXD1 */
        GPIO_InitStruct.Pin = GPIO_PIN_11 | GPIO_PIN_12 | GPIO_PIN_13;
        HAL_GPIO_Init(GPIOB, &GPIO_InitStruct);

        /* PC1=MDC, PC4=RXD0, PC5=RXD1 */
        GPIO_InitStruct.Pin = GPIO_PIN_1 | GPIO_PIN_4 | GPIO_PIN_5;
        HAL_GPIO_Init(GPIOC, &GPIO_InitStruct);

        HAL_NVIC_SetPriority(ETH_IRQn, 0, 0);
        HAL_NVIC_EnableIRQ(ETH_IRQn);
        HAL_NVIC_SetPriority(ETH_WKUP_IRQn, 0, 0);
        HAL_NVIC_EnableIRQ(ETH_WKUP_IRQn);
    }
}

void HAL_ETH_MspDeInit(ETH_HandleTypeDef *heth)
{
    if (heth->Instance == ETH)
    {
        HAL_NVIC_DisableIRQ(ETH_IRQn);
        HAL_NVIC_DisableIRQ(ETH_WKUP_IRQn);

        HAL_GPIO_DeInit(GPIOA, GPIO_PIN_1 | GPIO_PIN_2 | GPIO_PIN_7);
        HAL_GPIO_DeInit(GPIOB, GPIO_PIN_11 | GPIO_PIN_12 | GPIO_PIN_13);
        HAL_GPIO_DeInit(GPIOC, GPIO_PIN_1 | GPIO_PIN_4 | GPIO_PIN_5);

        __HAL_RCC_ETH1MAC_CLK_DISABLE();
        __HAL_RCC_ETH1TX_CLK_DISABLE();
        __HAL_RCC_ETH1RX_CLK_DISABLE();
    }
}

